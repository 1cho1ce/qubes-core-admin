import qubes.ext


class GUI(qubes.ext.Extension):
    # pylint: disable=too-few-public-methods
    # TODO put this somewhere...
    @staticmethod
    def send_gui_mode(vm):
        vm.run_service('qubes.SetGuiMode',
            input=('SEAMLESS'
            if vm.features.get('gui-seamless', False)
            else 'FULLSCREEN'))
